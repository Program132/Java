public class forc  {

    public static void main( String [] args ) {
        int a = 0;

        for (a = 0; a < 10; ++a)
        {
            System.out.println("\na = " + a);
        }

        for (int b = 5; b > 1; --b)
        {
            System.out.println("\nb = " + b);
        }
    }

}